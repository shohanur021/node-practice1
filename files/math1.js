
const add = (num1, num2) => {
    return num1+num2;
}

const sub = (num1, num2) => {
    return num1-num2;
}

const mul = (num1, num2) => {
    return num1*num2;
}

const div = (num1, num2) => {
    return num1/num2;
}


// export default add;    when only one value passed
export {add, sub, mul, div};